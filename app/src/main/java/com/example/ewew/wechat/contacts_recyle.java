package com.example.ewew.wechat;

public class contacts_recyle {
    private int image;
    private String text;

    public contacts_recyle(int image, String text) {
        this.image = image;
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
